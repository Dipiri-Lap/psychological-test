function getDimensionAnalysis(pattern) {
  // pattern 예: { S1:'L', S2:'M', S3:'L', E1:'L', ... }
  const dimOrder = ['S1','S2','S3','E1','E2','E3','A1','A2','A3','Ac1','Ac2','Ac3','So1','So2','So3'];
  const modelGroups = {
    "자아 모델": ['S1','S2','S3'],
    "감정 모델": ['E1','E2','E3'],
    "태도 모델": ['A1','A2','A3'],
    "행동 동력 모델": ['Ac1','Ac2','Ac3'],
    "사교 모델": ['So1','So2','So3']
  };

  const result = {};
  Object.entries(modelGroups).forEach(([modelName, dims]) => {
    result[modelName] = dims.map(dim => {
      const level = pattern[dim] || 'M';
      const dimData = KBTI_DIMENSION_DESC[dim];
      return {
        dim,
        label: dimData.label,
        emoji: dimData.emoji,
        level,
        title: dimData[level].title,
        desc: dimData[level].desc
      };
    });
  });

  return result;
}

