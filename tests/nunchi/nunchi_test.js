// ============================================
// 채점 함수
// ============================================

function calcNunchiScore(answers) {
  // answers = [{ 관계눈치:1, 공적눈치:0, SNS눈치:0, 결정눈치:1, 자기검열:0 }, ...] 20개

  const totals = { 관계눈치: 0, 공적눈치: 0, SNS눈치: 0, 결정눈치: 0, 자기검열: 0 };

  answers.forEach(a => {
    Object.keys(totals).forEach(key => {
      totals[key] += (a[key] || 0);
    });
  });

  // 각 성분 % 계산 (최대 40점 기준)
  const percentages = {};
  Object.keys(totals).forEach(key => {
    percentages[key] = Math.round((totals[key] / 40) * 100);
  });

  // 총 눈치 지수
  const totalScore = Object.values(totals).reduce((a, b) => a + b, 0);
  const maxTotal = 130;
  const totalPct = Math.min(100, Math.round((totalScore / maxTotal) * 100));

  // 등급 결정
  let grade;
  if (totalPct <= 20) grade = 'FREE';
  else if (totalPct <= 40) grade = 'AWARE';
  else if (totalPct <= 60) grade = 'EMPATH';
  else if (totalPct <= 80) grade = 'EXPERT';
  else grade = 'ULTRA';

  // 주성분 (가장 높은 눈치 유형)
  const mainComponent = Object.entries(percentages).sort((a, b) => b[1] - a[1])[0][0];

  return {
    totals,
    percentages,
    totalPct,
    grade,
    gradeInfo: NUNCHI_GRADES[grade],
    mainComponent,
    mainComponentInfo: NUNCHI_COMPONENTS[mainComponent]
  };
}

