// ============================================
// 채점 함수
// ============================================

function calcAiDependency(answers) {
  // answers = [{ 검색대체:1, 판단위임:0, ... }, ...] 12개
  const totals = { 검색대체: 0, 판단위임: 0, 창작의존: 0, 감정의존: 0, 습관화: 0 };

  answers.forEach(a => {
    Object.keys(totals).forEach(key => {
      totals[key] += (a[key] || 0);
    });
  });

  // 각 성분 % 계산 (최대 24점 기준)
  const percentages = {};
  Object.keys(totals).forEach(key => {
    percentages[key] = Math.round((totals[key] / 24) * 100);
  });

  // 총 의존 지수 (전체 평균)
  // 실제 최대 획득 가능 점수 = 99점 기준 (꼰대 테스트와 동일 방식)
  const totalScore = Object.values(totals).reduce((a, b) => a + b, 0);
  const maxTotal = 99;
  const totalPct = Math.round((totalScore / maxTotal) * 100);

  // 등급 결정
  let grade;
  if (totalPct <= 20) grade = 'PURE';
  else if (totalPct <= 40) grade = 'BALANCED';
  else if (totalPct <= 60) grade = 'LEANING';
  else if (totalPct <= 80) grade = 'HIGH';
  else grade = 'MASTER';

  // 가장 높은 성분 (주성분)
  const mainComponent = Object.entries(percentages).sort((a, b) => b[1] - a[1])[0][0];

  return {
    totals,
    percentages,
    totalPct,
    grade,
    gradeInfo: AI_GRADES[grade],
    mainComponent,
    mainComponentInfo: AI_COMPONENTS[mainComponent]
  };
}

