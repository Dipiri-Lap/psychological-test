// ============================================

function calcTravelType(answers) {
  // answers = [{ 계획, 즉흥, 활동, 휴식 }, ...] 12개
  const totals = { 계획: 0, 즉흥: 0, 활동: 0, 휴식: 0 };

  answers.forEach(a => {
    Object.keys(totals).forEach(key => {
      totals[key] += (a[key] || 0);
    });
  });

  const MAX_PER_AXIS = 18; // 6문항 × 최대 3점 (두 항목 합 = 3)

  const percentages = {
    계획: Math.round((totals.계획 / MAX_PER_AXIS) * 100),
    즉흥: Math.round((totals.즉흥 / MAX_PER_AXIS) * 100),
    활동: Math.round((totals.활동 / MAX_PER_AXIS) * 100),
    휴식: Math.round((totals.휴식 / MAX_PER_AXIS) * 100)
  };

  const isPlanner    = percentages.계획 >= 50;
  const isActive     = percentages.활동 >= 50;
  const typeCode = isPlanner
    ? (isActive ? 'PLANNER'    : 'CURATOR')
    : (isActive ? 'ADVENTURER' : 'WANDERER');

  return {
    totals,
    percentages,
    typeCode,
    typeInfo: TRAVEL_TYPES[typeCode]
  };
}

