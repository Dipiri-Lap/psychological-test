function getCompatibility(typeA, typeB) {
  const pair = [typeA, typeB].sort().join('_');
  return {
    pair,
    text: TRAVEL_COMPATIBILITY[pair],
    typeAInfo: TRAVEL_TYPES[typeA],
    typeBInfo: TRAVEL_TYPES[typeB]
  };
}

