function getComboNote(typeA, typeB) {
  if (typeA === typeB) return null;
  const key = [typeA, typeB].sort().join("_");
  return VARK_COMBO_NOTES[key];
}

// ============================================
// 채점 함수
// ============================================

function calcVarkType(answers) {
  // answers = ["V", "A", "R", "K", ...] 20개 (각 문항에서 선택한 type)
  const totals = { V: 0, A: 0, R: 0, K: 0 };

  answers.forEach(type => {
    if (totals[type] !== undefined) totals[type] += 1;
  });

  const maxScore = 20; // 문항 수
  const percentages = {};
  Object.keys(totals).forEach(key => {
    percentages[key] = Math.round((totals[key] / maxScore) * 100);
  });

  // 점수 내림차순 정렬
  const sorted = Object.entries(totals).sort((a, b) => b[1] - a[1]);
  const primaryType = sorted[0][0];
  const secondaryType = sorted[1][0];
  const isTie = sorted[0][1] === sorted[1][1]; // 1위, 2위 동점 여부 (혼합형 강조용)

  return {
    totals,
    percentages,
    primaryType,
    primaryTypeInfo: VARK_TYPES[primaryType],
    secondaryType,
    secondaryTypeInfo: VARK_TYPES[secondaryType],
    comboNote: getComboNote(primaryType, secondaryType),
    isTie
  };
}

