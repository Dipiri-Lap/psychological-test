function calcMbtiDependency(answers) {
  // answers = [{ 정보의존:1, 관계필터:0, 자기규정:0, 콘텐츠소비:0, 전파력:0 }, ...] 12개

  const totals = { 정보의존: 0, 관계필터: 0, 자기규정: 0, 콘텐츠소비: 0, 전파력: 0 };

  answers.forEach(a => {
    Object.keys(totals).forEach(key => {
      totals[key] += (a[key] || 0);
    });
  });

  // 각 성분 % 계산 (최대 24점 기준)
  const percentages = {};
  Object.keys(totals).forEach(key => {
    percentages[key] = Math.round((totals[key] / 24) * 100);
  });

  // 총 의존 지수
  const totalScore = Object.values(totals).reduce((a, b) => a + b, 0);
  const maxTotal = 99;
  const totalPct = Math.min(100, Math.round((totalScore / maxTotal) * 100));

  // 등급 결정
  let grade;
  if (totalPct <= 20) grade = 'FREE';
  else if (totalPct <= 40) grade = 'CASUAL';
  else if (totalPct <= 60) grade = 'MODERATE';
  else if (totalPct <= 80) grade = 'HIGH';
  else grade = 'ULTRA';

  // 주성분
  const mainComponent = Object.entries(percentages).sort((a, b) => b[1] - a[1])[0][0];

  return {
    totals,
    percentages,
    totalPct,
    grade,
    gradeInfo: MBTI_GRADES[grade],
    mainComponent,
    mainComponentInfo: MBTI_COMPONENTS[mainComponent]
  };
}

